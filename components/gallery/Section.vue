<template>
  <main class="gallery-section">
    <h3>{{ gallery.title }}</h3>
    <section class="gallery-section__images-wrapper">
      <div
        v-for="(image, index) in gallery.images"
        :key="index"
        class="gallery-section__image-container"
        @click="openLightBox(index)"
      >
        <h4>{{ image.title }}</h4>
        <img
          :src="require(`~/assets/images/${image}`)"
          class="gallery-section__image"
        />
      </div>
    </section>

    <GalleryLightbox
      v-if="isLightBoxOpen"
      class="gallery-section__lightbox"
      :images="gallery.images"
      :initial-index="initialImageIndex"
      @close-lightbox="closeLightBox"
    />
  </main>
</template>

<script>
export default {
  name: 'GallerySection',
  props: {
    gallery: {
      type: Object,
      required: true,
    },
  },
  data() {
    return {
      isLightBoxOpen: false,
      initialImageIndex: 0,
    };
  },
  methods: {
    openLightBox(index) {
      this.initialImageIndex = index;
      this.isLightBoxOpen = true;
    },
    closeLightBox() {
      this.isLightBoxOpen = false;
    },
  },
};
</script>

